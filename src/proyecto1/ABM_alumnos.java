    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String Nombre= txtnombre.getText();
         String Cedula= txtcedula.getText();
         if(Nombre.length()>0 || Cedula.length()>0){
        try {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO alumnos(nombre_alumno,documento_alumno) VALUES (?,?)");
            pst.setString(1, Nombre);
            pst.setString(2, Cedula); 
            pst.executeUpdate();
            mostrardatos("","");
        } catch (SQLException ex) {
            Logger.getLogger(ABM_alumnos.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
         else{
             showMessageDialog(null,"campos vacios");
         }
    }//GEN-LAST:event_jButton2ActionPerformed

