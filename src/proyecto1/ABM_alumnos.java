    void mostrardatos(String cedula, String nombre){
        DefaultTableModel modelo = new DefaultTableModel(); 
        
        modelo.addColumn ("Nombre");
        modelo.addColumn ("Cedula");
        modelo.addColumn ("ID");
        tablaAlumno.setModel(modelo);
        String sql = "";
        if(cedula.equals("") && nombre.equals("") ) 
            
     {
       sql = "SELECT * FROM alumnos";
     }
     else{
         if(nombre.equals("")) 
        {sql= "SELECT * FROM alumnos WHERE documento_alumno= '"+cedula+"'";}
        else{sql= "SELECT * FROM alumnos WHERE nombre_alumno= '"+nombre+"'";} 
    }
        
       String []datos = new String [3];
       
     try {
            Statement ts = cn.createStatement();
            ResultSet rs = ts.executeQuery(sql);
            
            while(rs.next()){
                datos[2]=rs.getString(1);
                datos[0]=rs.getString(2);
                datos[1]=rs.getString(3);
                modelo.addRow(datos);
            }
     
       tablaAlumno.setModel (modelo);
       TableColumnModel tcm = tablaAlumno.getColumnModel();
       tcm.removeColumn( tcm.getColumn(2) );
     } catch (SQLException ex) {
         Logger.getLogger(ABM_alumnos.class.getName()).log(Level.SEVERE, null, ex);   
     }
       
        
    }
    
    
    
    
    

